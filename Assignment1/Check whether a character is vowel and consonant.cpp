// Check whether a character is vowel and consonant

#include <iostream>
using namespace std;
int main() {
   char c = 'c';
   if (c == 'a' || c == 'e' || c == 'i' || c == 'o' || c == 'u' )
   cout <<c<< " is a Vowel" << endl;
   else
   cout <<c<< " is a Consonant" << endl;
   return 0;
}
